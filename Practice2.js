// Ye function check karta hai ki diya gaya number even hai ya odd

function checkEvenOdd(num) {
  if (num % 2 === 0) {
    console.log("Even Number");
  } 
  else {
    console.log("Odd Number");
  }
}

// Function call 
checkEvenOdd(10);   // Output: Even Number

// Function call 
checkEvenOdd(7);    // Output: Odd Number
